package for_loops;

public class MilkOnTheWall {
	public static void main(String[] args) {
		int n = 98;
		for(int i = n; i > -1; i--){
			System.out.println(++i +" bottles of milk on the wall, " +i +" bottles of milk! Take one down pass it around " +i-- +" bottles of milk on the wall!");
		
		}
	}

}
